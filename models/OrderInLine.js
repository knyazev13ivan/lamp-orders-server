import mongoose from "mongoose";

const OrderInLineSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    number: {
      type: Number,
      required: true,
    },
    priority: {
      type: Number,
      required: true,
    },
    text: {
      type: String,
      default: '',
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    }
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("OrderInLine", OrderInLineSchema);
